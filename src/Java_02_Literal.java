public class Java_02_Literal {
    static void main(String[] args) {
        byte age = 34; //34 is literal
        int age2 = 56;
        long num = 5666666666L;
        short age3 = 66;
        char ch = 'A'; //A is literal
        float f1 = 5.6f; //5.6f is literal
        double d1 = 4.66D; //4.66D is literal
        boolean a = true;
        String name = "Asha";
        System.out.println(age);
        System.out.println(name);
    }
}
